    private boolean isAddValueBusiness() {
        if (business.chkSetID(txtID.getText()) && business.chkSetTaxOffice(txtTaxOffice.getText().toUpperCase()) && business.chkSetCompRegNo(txtCompRegNo.getText()) && business.chkSetBusinessName(txtBusinessName.getText()) && business.chkSetNameSurname(txtNameSurname.getText().toUpperCase()) && business.chkSetPhone(txtPhone.getText()) && business.chkSetMail(txtMail.getText().toLowerCase()) && business.chkSetCountry(txtCountry.getText().toUpperCase()) && business.chkSetCity(txtCity.getText().toUpperCase()) && business.chkSetDistrict(txtDistrict.getText().toUpperCase()) && business.chkSetNightbrood(txtNightbrood.getText().toUpperCase()) && business.chkSetAddress(txtAddress.getText())) {
            return true;
        }

        JOptionPane.showMessageDialog(null, "Girdiğiniz format hatalıdır,\n\n* VKN/TC, Firma Adı, Adres Bilgileri boş geçilemez.\n* VKN/TC ve Telefon numarası sadece sayı girilmeli.\n* VKN/TC ve Telefon numaraları en fazla 15 karakter, Vergi Dairesi/TicarisiNo/İsim Soyisim/Mail bilgileri en fazla 50 karakter ve kalanlar en fazla 100 karakter girin.", "Hatalı Format", JOptionPane.ERROR_MESSAGE);
        return false;
    }

