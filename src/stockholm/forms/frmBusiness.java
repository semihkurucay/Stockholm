    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(null, "Firma ekleme işlemi yapıyorsunuz, emin misisniz?\n(Firma VKN/TC bir daha değişemez/güncellenemz!)", "Firma Ekleme", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION) {
            if (isAddValueBusiness()) {
                if (!sBusiness.isThereID(txtID.getText())) {
                    if (sBusiness.isAdd(business)) {
                        JOptionPane.showMessageDialog(null, "Firma ekleme işlemi başarıyla gerçekleşti!", "Başarılı Ekleme", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(null, "Firma ekleme işlemi başarısız oldu!", "Başarısız Ekleme", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Eklemek istediğiniz firma vkn/tc kayıtlı!", "Başarısız Ekleme", JOptionPane.ERROR_MESSAGE);
                }
            }
        }

        refresh();
    }//GEN-LAST:event_btnAddActionPerformed

