    private boolean isAddValueStock() {
        if (stock.chkSetID(txtID.getText().trim()) && stock.chkSetName(txtName.getText().trim()) && stock.chkSetUnitType(txtUnitType.getText().toUpperCase().trim()) && stock.chkSetPrice(txtPrice.getText().trim()) && stock.chkSetKdv(txtKDV.getText().trim())) {
            return true;
        }

        JOptionPane.showMessageDialog(null, "Girdiğiniz format hatalıdır,\n\n* Miktar hariç diğer bilgiler boş geçilemez.\n* Stok kodu boş geçilemez veya boşluk olamaz.\n* KDV ve Fiyat bilgisi sadece sayı olabailir.\n* Stok kodu en fazla 50 karakter olmalı.\n* Birim cinsi en fazla 20 karakter olmalı.", "Hatalı Format", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    private void addTextValue(cl_Stock _stock) {
        txtID.setText(_stock.getID());
        txtName.setText(_stock.getName());
        txtCategoryID.setText(_stock.getCategoryID());
        txtUnitType.setText(_stock.getUnitType());
        txtAmount.setText(_stock.getAmount() + "");
        txtKDV.setText(_stock.getKdv() + "");
        txtPrice.setText(_stock.getPrice() + "");

        lblSellPrice.setText(_stock.getPrice().add(_stock.getKdv().multiply(_stock.getPrice()).divide(new BigDecimal(100), 2, RoundingMode.HALF_UP)) + " TL");

        category = sCategory.getInformation(_stock.getCategoryID());
        txtCategoryName.setText(category.getName());
    }

    private void searchAddTextValue() {
        stock.setID(txtID.getText());
        stock.setName(txtName.getText());
        stock.setCategoryID(txtCategoryID.getText());
    }

