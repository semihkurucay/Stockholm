    private void txtCategoryIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCategoryIDActionPerformed
        // TODO add your handling code here:
        if (sCategory.isThereID(txtCategoryID.getText())) {
            category = sCategory.getInformation(txtCategoryID.getText());
            txtCategoryName.setText(category.getName());
            stock.setCategoryID(txtCategoryID.getText());
        } else {
            JOptionPane.showMessageDialog(null, "Girdiğiniz kategori kodu kayıtşı değil!", "Hatalı Kategori", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_txtCategoryIDActionPerformed

    private void txtPriceKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPriceKeyReleased
        // TODO add your handling code here:
        try {
            lblSellPrice.setText(new BigDecimal(txtPrice.getText()).add(new BigDecimal(txtKDV.getText()).multiply(new BigDecimal(txtPrice.getText())).divide(new BigDecimal(100), 2, RoundingMode.HALF_UP)) + " TL");
        } catch (Exception e) {
            lblSellPrice.setText("0.00 TL");
        }
    }//GEN-LAST:event_txtPriceKeyReleased

