    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(null, "Stok ekleme işlemi yapıyorsunuz, emin misisniz?\n(Stok kodu bir daha değişemez/güncellenemz!)", "Stok Ekleme", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION) {
            if (!(sStock.isThereID(txtID.getText())) && sCategory.isThereID(txtCategoryID.getText())) {
                if (isAddValueStock()) {
                    stock.setCategoryID(txtCategoryID.getText().trim());
                    if (sStock.isAdd(stock)) {
                        JOptionPane.showMessageDialog(null, "Stok ekleme işlemi başarıyla gerçekleşti!", "Başarılı Ekleme", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(null, "Stok ekleme işlemi başarısız oldu!", "Başarısız Ekleme", JOptionPane.ERROR_MESSAGE);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Stok kodu kayıtlı ya da Kategori kodu bulunamadı.", "Bulunamayan Veri", JOptionPane.ERROR_MESSAGE);
            }
        }

        refresh();
    }//GEN-LAST:event_btnAddActionPerformed

